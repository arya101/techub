let a=Number(prompt("enter first number"));
let b=Number(prompt("enter second number"));
let oper = prompt("enter operation");
switch (oper) {
    case '+':
    alert(a+b);
    break;

    case '-' :
    alert(a-b);
    break;

    case '*' :
    alert(a*b);
    break;
    
    case '/' :
    alert(a/b);
    break;

}